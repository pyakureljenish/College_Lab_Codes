//This is the program to find the size of the primitive data type
#include<iostream>
using namespace std;
int  main(){
    cout<<"The size of char is:"<<sizeof(char)<<'\n';
    cout<<"The size of float is:"<<sizeof(float)<<'\n';
    cout<<"The size of int is:"<<sizeof(int)<<'\n';
    cout<<"The size of double:"<<sizeof(double);

    return 0;

}